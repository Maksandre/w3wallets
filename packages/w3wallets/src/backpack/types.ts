/**
 * Represents the supported network types for the BackPack application.
 * - `Solana`: Used for Eclipse blockchain operations.
 * - `Eclipse`: Used for Eclipse blockchain operations.
 * - `Ethereum`: Used for Ethereum blockchain operations.
 * - `Polygon`: Used for Polygon blockchain operations (Ethereum Layer 2).
 * - `Base`: Used for Base blockchain operations (Ethereum Layer 2).
 * - `Arbitrum`: Used for Arbitrum blockchain operations (Ethereum Layer 2).
 * - `Optimism`: Used for Optimism blockchain operations (Ethereum Layer 2).
 */
export type BackPackNetwork = "Solana" | "Eclipse" | "Ethereum" | "Polygon" | "Base" | "Arbitrum" | "Optimism";
